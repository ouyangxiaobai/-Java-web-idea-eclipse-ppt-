package com.cuc.model;

public class BusinessStore {
	private int storeId;// 营业门店编号
	private String storeNo;// 营业门店账号
	private String storePassword;// 密码
	private String storeName;// 门店名称
	private String storeImage;// 门店照片
	private String storePhone;// 门店联系电话
	private String storeProvince;// 所在省份
	private String storeCity;// 所在城市
	private String storeDistrict;// 所在地区
	private String storeAddress;// 具体地址
	private int state;// 营业门店状态

	public int getStoreId() {
		return storeId;
	}

	public void setStoreId(int storeId) {
		this.storeId = storeId;
	}

	public String getStoreNo() {
		return storeNo;
	}

	public void setStoreNo(String storeNo) {
		this.storeNo = storeNo;
	}

	public String getStorePassword() {
		return storePassword;
	}

	public void setStorePassword(String storePassword) {
		this.storePassword = storePassword;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getStoreImage() {
		return storeImage;
	}

	public void setStoreImage(String storeImage) {
		this.storeImage = storeImage;
	}

	public String getStorePhone() {
		return storePhone;
	}

	public void setStorePhone(String storePhone) {
		this.storePhone = storePhone;
	}

	public String getStoreProvince() {
		return storeProvince;
	}

	public void setStoreProvince(String storeProvince) {
		this.storeProvince = storeProvince;
	}

	public String getStoreCity() {
		return storeCity;
	}

	public void setStoreCity(String storeCity) {
		this.storeCity = storeCity;
	}

	public String getStoreDistrict() {
		return storeDistrict;
	}

	public void setStoreDistrict(String storeDistrict) {
		this.storeDistrict = storeDistrict;
	}

	public String getStoreAddress() {
		return storeAddress;
	}

	public void setStoreAddress(String storeAddress) {
		this.storeAddress = storeAddress;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	
}
